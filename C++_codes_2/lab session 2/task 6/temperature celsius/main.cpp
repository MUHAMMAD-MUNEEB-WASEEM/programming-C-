//CONVERSION OF TEMPERATURE FROM CELSIUS TO FAHRENHEIT//
//INT(DATA TYPE)
#include<iostream>
using namespace std;
int main()
{
    int c,f;
    cout<<"Enter temperature in centigrade:";
    cin>>c;
    f=(9.0/5.0)*c+32.0;
    cout<<"Temperature in fahrenheit is"<<"\n"<<f;
}
//IN INT DATA TYPE OUTPUT OBTAINED IN INTEGER FORM MEANS THERE IS NO VALUE WITH DECIMAL
